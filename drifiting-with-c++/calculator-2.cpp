// Calculator 2 - long numbers

#include <iostream>
using namespace std;

int main () {
    long number1;
    long number2;

    cin >> number1;
    cin >> number2;

    cout << number1 << " + " << number2 << " = " << number1 + number2 << endl;
    cout << number1 << " - " << number2 << " = " << number1 - number2 << endl;
    cout << number1 << " * " << number2 << " = " << number1 * number2 << endl;
    cout << number1 << " / " << number2 << " = " << number1 / number2 << endl;
    cout << number1 << " % " << number2 << " = " << number1 % number2 << endl;
}
